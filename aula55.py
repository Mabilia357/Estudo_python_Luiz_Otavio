"""
Introdução ao desempacotamento 
"""
# exemplo de uso de desempacotamento de vairiaveis
_, _, nome, *resto = ['Maria', 'Helena', 'Luiz']
print(nome, resto)
# desempacottamento de string refere-se ao termo para uso dos dados presnetes em um
# comnjunto de dados, tipo nesse acima, temos 3 dados, e queremos apenas um, chama-se desempacottamento
# conforme uso de exemplo podemos desempacotar os dados.