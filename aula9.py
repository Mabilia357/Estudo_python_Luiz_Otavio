adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2 # em divisao não inteira, o resulatado sempre é float o resultado.
print('Divisão', divisao)

divisao_inteira = 10 // 2.2 # divisão inteira, mesmo com numero float, o resultado é ajustado para um numero float, ex: 2.0. 1.0.
print('Divisão inteira', divisao_inteira)

exponenciacao =  2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2 # resto da divisão
print('Modulo', modulo)

# alguns exemplos de uso do resto
print(10 % 8 == 0) # pergunto ao python se o resto da divisão é 0, ele retorna um boolean
print(16 % 8 == 0) # retorna um boolean
