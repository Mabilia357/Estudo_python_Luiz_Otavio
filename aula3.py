"""
Python = Linguagem de programção
Tipo de tipagem = Dinamica/Forte
str -> string -> texto entre em aspas
Strings são textos que estão dentro de aspas
"""
print(123)

# Aspas simples
print('Luiz Otavio')
print(1, 'Luiz "Otavio"') # Maneira de usar aspas duplas no string

# Aspas duplas
print("Luiz Otavio")
print(2, "Luiz 'Otavio'") # Maneira de usar aspas simples no string

# Escape
print("Luiz \"Otavio") # Maneira de usar aspas duplas no string usando barra invertida

# r
print(r"Luiz \"Otavio") # Maneira de usar a barra invetida na string

print('Explicito','é', 'melhor" do que implicito')