# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado
# postivo.
print(11) # tipo int -> inteiro
print (-11) # tipo int -> inteiro
print(0) # tipo int -> inteiro
# numero literal, literalmente um numero.

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) # tipo float -> ponto flutuante
print(0.0, -1.5) # tipo float -> contem um ponto flutuante

# A função type mostra o tipo que o Python
# inferiu ao valor. a função type é usada para varias coisas, mas nessa situação ela mostra o tipo de dado.
# observar o uso do ponto, virgula possui outra função e não define o dado float, como abaixo os exemplos.
print(type("otavio"))
print(type(0))
print(type(1.1), type(-1.1), type((0.0)))

