"""
For + Range
range -> range(start, stop, step)
"""
numeros = range(0, 100, 8) # uso da classe range, atributos incio(start), stop(até onde) e passo(step)
# nesse exemplo iniciamos com 0, até 100 em um passo de 8 em 8.

for numero in numeros: # exemplo de iterção com a classe range.
    print(numero)