# Tipo de dado bool (boolean)
# Ao questionar algo em um programa
# só existem duas respostas possíveis:
# sim (True) ou nao (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que 
# questiona se um valor é igual a outro, ou seja de certo modo questionamos o python perguntando se é True ou False.
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não => Não (Falso)
print(type(10 == 10)) # aqui ele somente destaca o tipo de dado, não descreve se é True ou False.
print(type(True))
print(type(False)) # da mesma forma se usarmos o type, já com o resultado de boleano, ele descreve o tipo.
